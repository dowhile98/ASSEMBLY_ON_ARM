

			;FUNCION QUE ELIMINA UNA CARACTER DE UN STRING
			AREA remove,CODE
			EXPORT	removeChar
			ALIGN
removeChar	PROC
			;R0->PUNTERO AL ARREGLO
			;R1->CARRACTER A ELIMINAR
			MOV		R2,R0		;SE RESPALDA EL PUNTERO DE LA CADENA
loop		LDRB	R3,[R0]		;SE LEE UN CARACTER DE LA CADENA
			CBZ		R3, exit	;SI SE LLEGA AL FINAL DE LA CADENA	
			CMP		R3,R1		;SE COMPARA LOS CARACTERES
			STRBNE	R3,[R2]		;SE ALMACENA UNA BYTE	
			ADDNE	R2,R2,#1	;s++
			ADD		R0,R0,#1
			B		loop
exit		STRB	R3,[R2]
			BX		LR
			ENDP
			end