;ESTE PROGRAMA CALCULA LA CANTIDAD DE DIGITOS DECIMALES QUE TIENE UN NUMERO ENTERO DADO
		AREA myCODE, CODE, READONLY,ALIGN=2
		EXPORT	__main
		ENTRY

__main	PROC
		LDR		R0,=123456;numero de entero de entrada
		MOV		R1,#0		;NUMERO DE DIGITOS
		
		MOV		R2,#10		;PARA QUE SE DIVIDA EL NUMERO
loop	CBZ		R0,stop		;SI R0 ES CERO
		SDIV	R0,R0,R2	;SE DIVIDE R0 = R0 / 10		
		ADD		R1,R1,#1	;COUNT ++
		B		loop

stop	B		stop
		
		ENDP
		END