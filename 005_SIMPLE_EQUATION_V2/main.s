; P = Q + R + S
;let Q=2, R=4,S=5


		AREA	myDATA, DATA, READONLY
				
P		SPACE	4	; RESERVA 4 BYTES EN LA MEMORIA
Q		DCD		2	; CREA UNA VARIABLE Q = 2
R		DCD		4	; CREA UNA VARIABLE R = 4
S		DCD		5	; CREA UNA VARIABLE S = 5
	
		AREA 	myCODE,CODE, READONLY
		ENTRY
		EXPORT	__main

__main	PROC			;SUBRUTINA PRINCIPAL
		LDR		R1,Q	;CARGA EL VALOR DE Q A R1
		LDR		R2,R	;CARGA EL VALOR DE R A R2
		LDR		R3,S	;CARGA EL VALOR DE S A R3	
		ADD		R0,R1,R2;R0 = R1 + R2
		ADD		R0,R0,R3;R0 = R0 + R3
Stop	B		Stop	;BUCLE INFINITO
				ENDP	;FIN DE LA SUBRUTINA PRINCIPAL
				END		;FIN DEL PROGRAMA