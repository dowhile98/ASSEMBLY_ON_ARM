;P = Q +S +R
;Q=2, R=4, S=5


;AREA DE CODIGO
		AREA	EQ3, CODE, READONLY
		ENTRY
		EXPORT	__main
__main	PROC				;SUBRUTINA PRINCIPAL
		ADRL	R0,P		;apunta a la direccion de P
		LDR		R1,[R0,#4]!	;CARGA EL VALOR DE Q Y ACTUALIZA R0 = R0 + 4
		LDR		R2,[R0,#4]!	;CARGA EL VALOR DE R Y ACTUALIZA R0 = R0 + 4
		ADD		R2,R2,R1	;R2 = R2 + R1
		LDR		R1,[R0,#4]	;CARGA EL VALOR DE S
		ADD		R2,R2,R1	;R2 = R2 + R1
Stop
		B		Stop
		ENDP				;FIN DE LA SUBRUTINA

;AREA DE DATOS
		AREA	EQ3, DATA, READONLY;SOLO LECTURA

P		SPACE		4		;RESERVA 4 BYTES EN LA MEMORIA
Q		DCD			2		;VARIABLE  CON VALOR INICIAL 2
R		DCD			4		;VARIABLE CON VALOR INICIAL 4
S		DCD			5		;VARIABLE CON VALOR INICIAL 5
		ALIGN				;ALINIACION DE DATOS EN BYTES
			
		END					;FIN DEL PROGRAMA